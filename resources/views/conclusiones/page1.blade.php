                    <div class="card-body">
                        <p>En conclusión, la legislación informática es un conjunto de normas y leyes que regulan el uso de las tecnologías de la información y la comunicación en la sociedad.</p>
                        <p>Es fundamental que existan leyes que protejan la privacidad y los datos personales de los individuos, así como que establezcan medidas para garantizar la seguridad de la información en un entorno digital cada vez más complejo y vulnerable a los ciberataques.</p>
                        <p>En México, existen diversas leyes y normas que regulan el uso de las tecnologías de la información y la comunicación, así como los delitos informáticos y la protección de los datos personales.
                        Es importante que los ciudadanos conozcan sus derechos y obligaciones en el entorno digital, así como las consecuencias legales de cometer delitos informáticos.</p>
